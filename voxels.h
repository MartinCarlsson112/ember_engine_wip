	struct voxel_chunk
	{
		dynamic_array<uint8_t> voxels;
		static uint8_t zero;

		uint8_t& operator[](uint32_t i) {
			
			if (!(i < voxels.size()))
			{
				return zero;
			}
			return voxels[i]; 
		}

		void dispose()
		{
			voxels.dispose();
		}
	};
	inline uint8_t voxel_chunk::zero = 0;

	inline constexpr uint32_t flatten_index(uint32_3 p, uint32_3 chunk_size)
	{
		return p.x + (p.y * chunk_size.x) +  (p.z * chunk_size.x * chunk_size.y);
	}

	inline constexpr float3 get_vertex(float3 p1, float3 p2)
	{
		float mu = 0.5;
		float3 p;
		p.x = p1.x + mu * (p2.x - p1.x);
		p.y = p1.y + mu * (p2.y - p1.y);
		p.z = p1.z + mu * (p2.z - p1.z);
		return p;
	}

	inline constexpr float3 get_voxel_pos(uint32_t index, uint32_3 chunk_size, float voxel_size)
	{
		float3 res;
		res.x = (float)(index % chunk_size.x);
		res.y = (float)((index / chunk_size.x) % chunk_size.y);
		res.z = (float)(index / (chunk_size.x * chunk_size.y));

		res.x *= voxel_size;
		res.y *= voxel_size;
		res.z *= voxel_size;

		return res;
	}

	inline constexpr float3 get_chunk_pos(uint32_t index, uint32_3 chunk_size, float voxel_size)
	{
		float3 chunk_pos = get_voxel_pos(index, chunk_size, voxel_size);
		chunk_pos.x *= chunk_size.x;
		chunk_pos.y *= chunk_size.y;
		chunk_pos.z *= chunk_size.z;
		return chunk_pos;
	}

	inline constexpr uint32_2 get_chunk_index(uint32_3 p, uint32_3 chunk_size)
	{
		uint32_3 v = uint32_3(p.x % chunk_size.x, p.y % chunk_size.y, p.z % chunk_size.z);
		uint32_t linear_pos = flatten_index(v, chunk_size); 
		uint32_3 s = uint32_3(p.x / chunk_size.x, p.y / chunk_size.y, p.z / chunk_size.z);
		uint32_t chunk_pos = flatten_index(s, chunk_size);
		return { chunk_pos, linear_pos };
	}

	struct voxel_storage
	{
		voxel_storage(uint32_3 chunk_size) : chunk_size(chunk_size) { total_chunk_size = chunk_size.x * chunk_size.y * chunk_size.z; }

		uint32_t add_color(uint32_4 color)
		{
			colors.push_back(color);
			return colors.size() - 1;
		}

		void set_voxel(uint32_3 p, uint32_t color)
		{
			uint32_2 chunk_index = get_chunk_index(p, chunk_size);

			if (chunk_index.x >= chunks.size())
			{
				chunks.resize(chunk_index.x + 1);
			}

			auto i = chunks[chunk_index.x].voxels.size();

			if (chunk_index.y >= i)
			{
				chunks[chunk_index.x].voxels.resize(chunk_index.y + 1);
			}
			chunks[chunk_index.x][chunk_index.y] = color;
		}

		uint8_t& get_voxel(uint32_3 p)
		{
			uint32_2 chunk_index = get_chunk_index(p, chunk_size);

			return chunks[chunk_index.x][chunk_index.y];
		}

		uint32_t operator[](uint32_t i) const
		{
			uint32_t chunk_index = i / total_chunk_size;
			uint32_t index = i % total_chunk_size;

			if (chunks.size() > chunk_index)
			{
				if (chunks[chunk_index].voxels.size() > index)
				{
					return chunks[chunk_index][index];
				}
			}
			return 0;
		}

		void dispose()
		{
			for (uint32_t i = 0; i < chunks.size(); i++)
			{
				chunks[i].dispose();
			}
			colors.dispose();
		}

		uint32_t total_chunk_size;
		uint32_3 chunk_size;
		dynamic_array<uint32_4> colors;
		dynamic_array<voxel_chunk> chunks;
	};

